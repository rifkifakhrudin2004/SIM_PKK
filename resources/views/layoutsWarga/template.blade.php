                <script>
                    // Fungsi untuk membuka popup dan menetapkan gambar dan deskripsi
                    function openPopup(imageSrc, description) {
                        var popup = document.getElementById('popup');
                        var image = document.getElementById('popup-image');
                        var desc = document.getElementById('popup-description');

                        image.src = imageSrc;
                        desc.textContent = description;

                        popup.style.display = "flex";
                    }

                    // Fungsi untuk menutup popup
                    function closePopup() {
                        var popup = document.getElementById('popup');
                        popup.style.display = "none";
                    }

                    // Ambil elemen HTML yang dibutuhkan
                    const popupImage = document.getElementById('popup-image');
                    const popupDescription = document.getElementById('popup-description');

                    // Fetch data dari endpoint backend
                    fetch('/get-image-and-description')
                        .then(response => response.json())
                        .then(data => {
                            // Tampilkan gambar pada elemen #popup-image
                            popupImage.src = data.foto_konten;

                            // Tampilkan deskripsi pada elemen #popup-description
                            popupDescription.textContent = data.deskripsi_konten;
                        })
                        .catch(error => {
                            console.error('Error fetching image and description:', error);
                        });

                    // Tambahkan event listener pada tombol yang akan membuka popup
                    const openPopupButton = document.querySelector('#open-popup-button');
                    openPopupButton.addEventListener('click', () => {
                        // Munculkan popup
                        const popupContainer = document.querySelector('#popup');
                        popupContainer.style.display = 'block';
                    });
